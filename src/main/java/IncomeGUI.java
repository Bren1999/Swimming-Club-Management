    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jPanel3.setVisible(true); //make the section to display the income visible 
        int yearSearch = Integer.parseInt(yearInp.getText()); //get the year 
        HashMap<String,CustomerIncome> customerIncome = new HashMap<>();
        try (Scanner scanner = new Scanner(new File(fileName));) {
            while (scanner.hasNextLine()) {
                String row[] = scanner.nextLine().split(",");
                if(row.length>=13){ //get all the rows from the csv, if the income is present.(size less than 13 means old record)
                    String date[] = row[10].split("/");
                    int day = Integer.parseInt(date[0]);
                    Month month = Month.of(Integer.parseInt(date[1]));
                    int year = Integer.parseInt(date[2]);
                    customerIncome.put(row[0], new CustomerIncome(row[0],new Date(day,month,year),Double.parseDouble(row[12])));
                }
            }
        }catch (IOException e) {
            e.printStackTrace();
        }
        
        Income income = new Income(customerIncome);
        jLabel3.setText("£"+String.valueOf(income.getYearlyIncome(yearSearch)));
        //display on the table for each month
        DefaultTableModel table = (DefaultTableModel)jTable1.getModel();
        table.setRowCount(0);
        for(Month month : Month.values()){
            String incomeRow[] = {month.toString(),"£"+String.valueOf(income.getMonthlyIncome(month, yearSearch))};
            table.addRow(incomeRow);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

